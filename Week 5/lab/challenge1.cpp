#include<iostream>
using namespace std;
main(){

    int i,j;
    for(int i=1; i<=5; i++){
         for(int j=1; j<=i; j++ ){
           cout<<"*";
    }
    cout<<""<<endl;
}

int k,l;
for(int k=5; k>=1; k--){
    for(int l=1; l<=k; l++){
        cout<<"*";
    }
    cout<<""<<endl;
}
       
    
}